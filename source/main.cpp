	//---------------------------------------------------------------------------------
	int i;

	Camera camera((u16*) world);
	
	Player player((u8*)marioTiles, 0, 136, 0, 0);

	consoleDemoInit();
	
	//set video mode and map vram to the background
	videoSetMode(MODE_0_2D | DISPLAY_BG0_ACTIVE);
